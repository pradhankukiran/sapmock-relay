// Create the SnapLogic Mapper Tab
export function createSnapLogicTab(controls: any, data: any): any {
  const { VBox, Label, CodeEditor, Select, Item, Card, CardHeader } = controls;

  const selectContract = new Select({
    width: "100%"
  });

  data.contracts.forEach((itemVal: any) => {
    selectContract.addItem(new Item({ key: itemVal.id, text: `${itemVal.method} ${itemVal.path} (${itemVal.system})` }));
  });

  const outputEditor = new CodeEditor({
    width: "100%",
    height: "320px",
    type: "json",
    editable: false,
    value: ""
  });

  const updateMappings = () => {
    const selectedKey = selectContract.getSelectedKey();
    const contract = data.contracts.find((c: any) => c.id === selectedKey);
    const mappings = generateSnapLogicMappings(contract);
    outputEditor.setValue(mappings);
  };

  selectContract.attachChange(updateMappings);

  // Trigger initial generate
  setTimeout(updateMappings, 200);

  const viewContainer = new VBox({
    width: "100%",
    items: [
      new Label({ text: "Select Target Contract Specification" }).addStyleClass("sapUiTinyMarginBottom"),
      selectContract,
      new Label({ text: "Generated SnapLogic Mapper Snap Expression Mappings" }).addStyleClass("sapUiMediumMarginTop sapUiTinyMarginBottom"),
      outputEditor
    ]
  });

  return new Card({
    header: new CardHeader({
      title: "SnapLogic JSON-to-SAP Field Mapper",
      subtitle: "Generate property translations between Fiori REST schemas and backend BAPI inputs",
      iconSrc: "sap-icon://arrow-right"
    }),
    content: new VBox({
      items: [viewContainer]
    }).addStyleClass("sapUiContentPadding")
  });
}

// Create the Fiori Launchpad Tile Designer Tab
export function createLaunchpadTab(controls: any): any {
  const { VBox, HBox, Label, Input, Select, Item, CodeEditor, Card, CardHeader, Text } = controls;

  // Local tile preview states
  const formState = {
    title: "QM Supplier Console",
    subtitle: "Track Quality Notifications",
    semanticObject: "QMNotification",
    action: "display",
    tileType: "sap.ushell.ui.tile.StaticTile",
    icon: "sap-icon://quality",
    url: "/sap/bc/ui5_ui5/sap/zqm_supplier"
  };

  // Form controls
  const titleInput = new Input({
    value: formState.title,
    liveChange: (event: any) => {
      formState.title = event.getParameter("value");
      updateAll();
    }
  });

  const subtitleInput = new Input({
    value: formState.subtitle,
    liveChange: (event: any) => {
      formState.subtitle = event.getParameter("value");
      updateAll();
    }
  });

  const semObjInput = new Input({
    value: formState.semanticObject,
    liveChange: (event: any) => {
      formState.semanticObject = event.getParameter("value");
      updateAll();
    }
  });

  const actionInput = new Input({
    value: formState.action,
    liveChange: (event: any) => {
      formState.action = event.getParameter("value");
      updateAll();
    }
  });

  const iconInput = new Input({
    value: formState.icon,
    liveChange: (event: any) => {
      formState.icon = event.getParameter("value");
      updateAll();
    }
  });

  const tileTypeSelect = new Select({
    selectedKey: formState.tileType,
    change: (event: any) => {
      formState.tileType = event.getSource().getSelectedKey();
      updateAll();
    }
  });
  tileTypeSelect.addItem(new Item({ key: "sap.ushell.ui.tile.StaticTile", text: "Static App Tile" }));
  tileTypeSelect.addItem(new Item({ key: "sap.ushell.ui.tile.DynamicTile", text: "Dynamic Count Tile" }));

  // Live preview element (Mock UI5 HTML rendering)
  // We can build a custom HTML wrapper block or a clean Fiori tile mockup using standard VBox elements
  const previewTitle = new Text({ text: formState.title }).addStyleClass("sapmockTileTitle");
  const previewSubtitle = new Text({ text: formState.subtitle }).addStyleClass("sapmockTileSubtitle");
  const previewNumber = new Text({ text: "12" }).addStyleClass("sapmockTileNumber");
  
  // Set initial dynamic visibility
  previewNumber.setVisible(false);

  const previewBox = new VBox({
    width: "150px",
    height: "150px",
    items: [
      previewTitle,
      previewSubtitle,
      new VBox({
        alignItems: "End",
        justifyContent: "End",
        width: "100%",
        items: [previewNumber]
      }).addStyleClass("sapUiMediumMarginTop")
    ]
  }).addStyleClass("sapMGT"); // Styled using our generic tile CSS class!

  const configEditor = new CodeEditor({
    width: "100%",
    height: "260px",
    type: "json",
    editable: false,
    value: ""
  });

  function updateAll() {
    previewTitle.setText(formState.title);
    previewSubtitle.setText(formState.subtitle);
    if (formState.tileType === "sap.ushell.ui.tile.DynamicTile") {
      previewNumber.setVisible(true);
    } else {
      previewNumber.setVisible(false);
    }
    const config = generateFioriLaunchpadConfig(formState);
    configEditor.setValue(config);
  }

  // Initial trigger
  setTimeout(updateAll, 200);

  // Field helpers
  function formField(labelText: string, control: any) {
    return new VBox({
      width: "100%",
      items: [
        new Label({ text: labelText }),
        control
      ]
    }).addStyleClass("sapmockField");
  }

  const formLayout = new HBox({
    width: "100%",
    items: [
      formField("Tile Title", titleInput),
      formField("Tile Subtitle", subtitleInput),
      formField("Semantic Object", semObjInput),
      formField("Action", actionInput),
      formField("Launchpad Icon", iconInput),
      formField("Tile Mode Type", tileTypeSelect)
    ]
  }).addStyleClass("sapmockFormGrid");

  const previewLayout = new HBox({
    width: "100%",
    alignItems: "Start",
    items: [
      new VBox({
        width: "200px",
        items: [
          new Label({ text: "Launchpad Tile Preview" }).addStyleClass("sapUiTinyMarginBottom"),
          previewBox
        ]
      }),
      new VBox({
        width: "100%",
        items: [
          new Label({ text: "Target Mapping Configuration Export" }).addStyleClass("sapUiTinyMarginBottom"),
          configEditor
        ]
      }).addStyleClass("sapUiSmallMarginBegin")
    ]
  }).addStyleClass("sapUiLargeMarginTop");

  return new Card({
    header: new CardHeader({
      title: "Fiori Launchpad Designer & Tile Configuration",
      subtitle: "Model target mappings, preview tiles, and generate catalog configuration entries",
      iconSrc: "sap-icon://grid"
    }),
    content: new VBox({
      items: [formLayout, previewLayout]
    }).addStyleClass("sapUiContentPadding")
  });
}

// Create the OpenAPI Explorer Tab
export function createOpenApiExplorerTab(controls: any, data: any, selectContract: any): any {
  const { VBox, HBox, Label, Text, Button, Card, CardHeader, ObjectStatus, ObjectIdentifier, CodeEditor } = controls;

  const explorerContainer = new VBox({
    width: "100%"
  });

  data.contracts.forEach((contract: any, index: number) => {
    const methodColor = contract.method === "GET" ? "Success" : contract.method === "POST" ? "Information" : "Warning";
    
    const routeHeader = new HBox({
      alignItems: "Center",
      justifyContent: "SpaceBetween",
      width: "100%",
      items: [
        new HBox({
          alignItems: "Center",
          items: [
            new ObjectStatus({ text: contract.method, state: methodColor }).addStyleClass("sapUiTinyMarginEnd"),
            new ObjectIdentifier({ title: contract.path, text: contract.summary })
          ]
        }),
        new Button({
          text: "Load in Request Runner",
          icon: "sap-icon://process",
          type: "Emphasized",
          press: () => {
            // Load and switch
            selectContract(contract.id);
          }
        })
      ]
    }).addStyleClass("sapUiSmallMarginBottom");

    const schemaInfo = new VBox({
      width: "100%",
      items: [
        new Label({ text: "Integration Scope Target System" }).addStyleClass("sapUiTinyMarginBottom"),
        new Text({ text: `SAP Backend System ID: ${contract.system}` }).addStyleClass("sapUiSmallMarginBottom"),
        new Label({ text: "API Response Specification Schema" }).addStyleClass("sapUiTinyMarginBottom"),
        new CodeEditor({
          width: "100%",
          height: "150px",
          type: "json",
          editable: false,
          value: JSON.stringify(contract.responseSchema, null, 2)
        })
      ]
    }).addStyleClass("sapUiContentPadding");

    const contractCard = new Card({
      header: new CardHeader({
        title: `${contract.method} ${contract.path}`,
        subtitle: contract.summary,
        iconSrc: "sap-icon://document-text"
      }),
      content: new VBox({
        items: [schemaInfo, routeHeader]
      }).addStyleClass("sapUiContentPadding")
    }).addStyleClass(index > 0 ? "sapUiMediumMarginTop" : "");

    explorerContainer.addItem(contractCard);
  });

  return new Card({
    header: new CardHeader({
      title: "Interactive OpenAPI Schema Explorer",
      subtitle: "Drill down into REST integration contracts and map direct endpoints to the request simulator",
      iconSrc: "sap-icon://search"
    }),
    content: explorerContainer
  }).addStyleClass("sapUiContentPadding");
}
