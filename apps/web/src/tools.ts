// Global reference mapping for generating ABAP variable names
function sanitizeAbapName(name: string): string {
  let sanitized = name.replace(/[^a-zA-Z0-9_]/g, "_").toUpperCase();
  if (sanitized.length > 30) {
    sanitized = sanitized.slice(0, 30);
  }
  // Ensure it doesn't start with a number
  if (/^[0-9]/.test(sanitized)) {
    sanitized = "V_" + sanitized;
  }
  return sanitized;
}

// Convert JSON object structure to ABAP structures recursively
function jsonToAbapTypes(obj: unknown, typeName: string, generatedTypes: string[]): string {
  if (obj === null || obj === undefined) {
    return `${sanitizeAbapName(typeName)} TYPE string`;
  }

  if (Array.isArray(obj)) {
    if (obj.length === 0) {
      return `${sanitizeAbapName(typeName)} TYPE string_table`;
    }
    const sampleItem = obj[0];
    if (typeof sampleItem === "object" && sampleItem !== null) {
      const elementTypeName = `TY_${sanitizeAbapName(typeName)}_ITEM`;
      const structDef = jsonToAbapTypes(sampleItem, elementTypeName, generatedTypes);
      generatedTypes.push(structDef);
      return `${sanitizeAbapName(typeName)} TYPE STANDARD TABLE OF ${elementTypeName} WITH DEFAULT KEY`;
    } else {
      const typeStr = typeof sampleItem === "number" ? "INT4" : typeof sampleItem === "boolean" ? "ABAP_BOOL" : "STRING";
      return `${sanitizeAbapName(typeName)} TYPE STANDARD TABLE OF ${typeStr} WITH DEFAULT KEY`;
    }
  }

  if (typeof obj === "object" && obj !== null) {
    const lines: string[] = [];
    lines.push(`TYPES: BEGIN OF ${sanitizeAbapName(typeName)},`);
    
    Object.entries(obj).forEach(([key, val]) => {
      const sanitizedKey = sanitizeAbapName(key);
      if (typeof val === "object" && val !== null) {
        const nestedTypeName = `TY_${sanitizeAbapName(typeName)}_${sanitizedKey}`;
        const fieldDef = jsonToAbapTypes(val, nestedTypeName, generatedTypes);
        // If it returns a full declaration, it was parsed nested, add it to generated types
        if (fieldDef.startsWith("TYPES: BEGIN OF")) {
          generatedTypes.push(fieldDef);
          if (Array.isArray(val)) {
            lines.push(`         ${sanitizedKey} TYPE STANDARD TABLE OF ${nestedTypeName}_ITEM WITH DEFAULT KEY,`);
          } else {
            lines.push(`         ${sanitizedKey} TYPE ${nestedTypeName},`);
          }
        } else {
          // It's a table/nested definition returned directly
          lines.push(`         ${sanitizedKey} TYPE ${nestedTypeName},`);
        }
      } else {
        let typeStr = "STRING";
        if (typeof val === "number") {
          typeStr = Number.isInteger(val) ? "INT4" : "DECFLOAT34";
        } else if (typeof val === "boolean") {
          typeStr = "ABAP_BOOL";
        }
        lines.push(`         ${sanitizedKey} TYPE ${typeStr},`);
      }
    });

    lines.push(`       END OF ${sanitizeAbapName(typeName)}.`);
    return lines.join("\n");
  }

  return "";
}

// Primary JSON to ABAP Parser logic
export function generateAbapCode(jsonStr: string): string {
  try {
    const parsed = JSON.parse(jsonStr);
    const generatedTypes: string[] = [];
    const rootDef = jsonToAbapTypes(parsed, "TY_ROOT", generatedTypes);
    
    const allTypes = [...generatedTypes, rootDef].join("\n\n");
    
    return `*----------------------------------------------------------------------*
* ABAP Type Declarations & Parser Code
* Generated by SAPMock Relay Console
*----------------------------------------------------------------------*

${allTypes}

DATA: lv_json_payload TYPE string,
      ls_parsed_data  TYPE ty_root,
      lo_error        TYPE REF TO cx_root.

lv_json_payload = '${jsonStr.replace(/'/g, "''").replace(/\n/g, "' && \n'").slice(0, 1000)}${jsonStr.length > 1000 ? "... [truncated]" : ""}'.

TRY.
    " Deserialize JSON payload using standard SAP utility
    /ui2/cl_json=>deserialize(
      EXPORTING
        json             = lv_json_payload
        assoc_to_camel_case = abap_true
        pretty_name      = /ui2/cl_json=>pretty_name-camel_case
      CHANGING
        data             = ls_parsed_data
    ).
    
    " ls_parsed_data is now populated and ready for business logic
  CATCH cx_root INTO lo_error.
    " Handle parsing errors here
ENDTRY.`;
  } catch (error) {
    return `* Error parsing JSON: ${(error as Error).message}`;
  }
}

// Generate SnapLogic expression block based on target contract
export function generateSnapLogicMappings(contract: any): string {
  if (!contract) return "/* Please select a contract specification */";
  const schema = contract.responseSchema;
  const properties = schema?.properties || {};
  const mappings: Record<string, string> = {};

  Object.keys(properties).forEach((key) => {
    // Map standard camelCase REST keys to standard SAP uppercase BAPI structures
    const bapiField = sanitizeAbapName(key);
    mappings[bapiField] = `$${key}`;
  });

  return JSON.stringify(mappings, null, 2);
}

// Generate Fiori Launchpad target mapping export
export function generateFioriLaunchpadConfig(tileState: {
  title: string;
  subtitle: string;
  semanticObject: string;
  action: string;
  tileType: string;
  icon: string;
  url: string;
}): string {
  return JSON.stringify(
    {
      "sap.app": {
        "id": `sap.mock.relay.${tileState.semanticObject.toLowerCase()}.${tileState.action.toLowerCase()}`,
        "type": "application",
        "title": tileState.title,
        "description": tileState.subtitle
      },
      "sap.ui": {
        "technology": "UI5",
        "deviceTypes": {
          "desktop": true,
          "tablet": true,
          "phone": true
        }
      },
      "sap.platform.abap": {
        "uri": tileState.url
      },
      "launchpad": {
        "tileType": tileState.tileType,
        "title": tileState.title,
        "subtitle": tileState.subtitle,
        "icon": tileState.icon,
        "navigation": {
          "semanticObject": tileState.semanticObject,
          "action": tileState.action,
          "parameters": {}
        }
      }
    },
    null,
    2
  );
}

